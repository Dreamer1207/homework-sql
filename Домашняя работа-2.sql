CREATE TABLE IF NOT EXISTS Genre ( 
	id SERIAL PRIMARY KEY,
	name VARCHAR(40) NOT NULL
);

CREATE TABLE IF NOT EXISTS Performer (
	id SERIAL PRIMARY KEY,
	name VARCHAR(40) NOT NULL
);

CREATE TABLE IF NOT EXISTS GenrePerformer (
	id SERIAL PRIMARY KEY,
	genre_id INTEGER NOT NULL REFERENCES Genre(id),
	performer_id INTEGER NOT NULL REFERENCES Performer(id)
);

CREATE TABLE IF NOT EXISTS Album (
	id SERIAL PRIMARY KEY,
	name VARCHAR(40) NOT NULL,
	year_release INTERVAL YEAR NOT NULL	
);

CREATE TABLE IF NOT EXISTS PerformerAlbum (
	id SERIAL PRIMARY KEY,
	performer_id INTEGER NOT NULL REFERENCES Performer(id),
	album_id INTEGER NOT NULL REFERENCES Album(id)
);

CREATE TABLE IF NOT EXISTS Track (
	id SERIAL PRIMARY KEY,
	name VARCHAR(40) NOT NULL,
	duration INTERVAL MINUTE TO SECOND NOT NULL,
	album_id INTEGER NOT NULL REFERENCES Album(id)
);

CREATE TABLE IF NOT EXISTS Сollection ( 
	id SERIAL PRIMARY KEY,
	name VARCHAR(40) NOT NULL,
	year_release INTERVAL YEAR NOT NULL
);

CREATE TABLE IF NOT EXISTS СollectionTrack (
	id SERIAL PRIMARY KEY,
	сollection_id INTEGER NOT NULL REFERENCES Сollection(id),
	track_id INTEGER NOT NULL REFERENCES Track(id)	
);


